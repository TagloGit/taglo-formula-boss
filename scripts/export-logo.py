def create_icon(size):
    src = Image.new("RGBA", (20, 20))
    for y, row in enumerate(GRID):
        for x, c in enumerate(row):
            src.putpixel((x, y), PALETTE[c])
    return src.resize((size, size), Image.NEAREST)

assets_dir = os.path.join(os.path.dirname(__file__), "..", "assets")
os.makedirs(assets_dir, exist_ok=True)

for name, size in [("logo-16.png", 16), ("logo-32.png", 32), ("logo-128.png", 128), ("logo-256.png", 256)]:
    img = create_icon(size)
    path = os.path.join(assets_dir, name)
    img.save(path)
    print(f"Exported {path} ({size}x{size})")

